char	*ft_strchr(const char *s, int c)
{
	char	x;

	x = (char)c;
	while ((*s != '\0') && (*s != x))
		s++;
	if ((*s == '\0') && (*s != x))
		return (0);
	else
		return ((char *)s);
}
